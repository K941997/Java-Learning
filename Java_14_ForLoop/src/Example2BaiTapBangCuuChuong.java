
public class Example2BaiTapBangCuuChuong {
	public static void main(String[] args) {
		//Vòng lặp ForLoop dùng khi có điểm dừng
		//Bai tap: For Loop tao bang cuu chuong (Vòng lặp ForLoop)
		
		for (int i = 2; i <= 9; i++) {
			for (int j = 1; j <= 10; j++) {
				System.out.println(i+ " x " +j+ " = " +(i*j));
			}
			System.out.println("------");
		}
	}
}
