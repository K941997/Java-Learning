
public class Example1BaiTapChepPhat {
	public static void main(String[] args) {
		//Vòng lặp ForLoop dùng khi có điểm dừng
		//Bai tap: For Loop Chep Phat 20 lan "Em hua lam bai day du" (Vòng lặp ForLoop)
		
		for (int i = 0; i < 20; i++) {
			System.out.println(i+ ". Em hua lam bai day du");
		}
		
		for (int i = 20; i > 0; i--) {
			System.out.println(i+". Em hua lam bai day du");
		}
	}
}	
